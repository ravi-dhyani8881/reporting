package com.ytk.models;

import org.codehaus.jackson.annotate.JsonProperty;
import org.codehaus.jackson.annotate.JsonWriteNullProperties;


@JsonWriteNullProperties(false)
public class HeaderPlace {
	
	private String  ID; 
	private String ScreenName;
	private String ProfileImage;
	private String Category1Name;
	private String  City;
	private String State;
	private String TotalReviews;
	private String Address1 ;
	private String ZipCode ;
	private String Phone ;
	private String NeighbourHoods; 
	private String Name ;
	private String ProfileFileJson="";
	
	@JsonProperty("ID")
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	
	@JsonProperty("ScreenName")
	public String getScreenName() {
		return ScreenName;
	}
	public void setScreenName(String screenName) {
		ScreenName = screenName;
	}
	
	@JsonProperty("ProfileImage")
	public String getProfileImage() {
		return ProfileImage;
	}
	public void setProfileImage(String profileImage) {
		ProfileImage = profileImage;
	}
	
	@JsonProperty("Category1Name")
	public String getCategory1Name() {
		return Category1Name;
	}
	public void setCategory1Name(String category1Name) {
		Category1Name = category1Name;
	}
	
	@JsonProperty("City")
	public String getCity() {
		return City;
	}
	public void setCity(String city) {
		City = city;
	}
	
	@JsonProperty("State")
	public String getState() {
		return State;
	}
	public void setState(String state) {
		State = state;
	}
	
	@JsonProperty("TotalReviews")
	public String getTotalReviews() {
		return TotalReviews;
	}
	public void setTotalReviews(String totalReviews) {
		TotalReviews = totalReviews;
	}
	
	@JsonProperty("Address1")
	public String getAddress1() {
		return Address1;
	}
	public void setAddress1(String address1) {
		Address1 = address1;
	}
	
	@JsonProperty("ZipCode")
	public String getZipCode() {
		return ZipCode;
	}
	public void setZipCode(String zipCode) {
		ZipCode = zipCode;
	}
	
	@JsonProperty("Phone")
	public String getPhone() {
		return Phone;
	}
	public void setPhone(String phone) {
		Phone = phone;
	}
	
	@JsonProperty("NeighbourHoods")
	public String getNeighbourHoods() {
		return NeighbourHoods;
	}
	public void setNeighbourHoods(String neighbourHoods) {
		NeighbourHoods = neighbourHoods;
	}
	
	@JsonProperty("Name")
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
	@JsonProperty("ProfileFileJson")
	public String getProfileFileJson() {
		return ProfileFileJson;
	}
	public void setProfileFileJson(String profileFileJson) {
		ProfileFileJson = profileFileJson;
	}
	
	
	
	
	
	
}
