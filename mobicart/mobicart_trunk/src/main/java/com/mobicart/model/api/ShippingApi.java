package com.mobicart.model.api;

import java.math.BigDecimal;

import org.codehaus.jackson.annotate.JsonWriteNullProperties;
@JsonWriteNullProperties(false)
public class ShippingApi {
 
	private Long shippingId;
	private Long storeId;
	private Long countryId;
	private Long stateId;
	private String shippingCountry;
	private String shippingState;
	
	private BigDecimal shippingAmountSingleProduct;
	private BigDecimal shippingAmountMultipleProduct;
	private BigDecimal ShippingTax;



	public Long getShippingId() {
		return shippingId;
	}


	public void setShippingId(Long shippingId) {
		this.shippingId = shippingId;
	}


	public Long getStoreId() {
		return storeId;
	}


	public void setStoreId(Long storeId) {
		this.storeId = storeId;
	}


	public Long getCountryId() {
		return countryId;
	}






	public void setCountryId(Long countryId) {
		this.countryId = countryId;
	}






	public Long getStateId() {
		return stateId;
	}






	public void setStateId(Long stateId) {
		this.stateId = stateId;
	}






	public String getShippingCountry() {
		return shippingCountry;
	}






	public void setShippingCountry(String shippingCountry) {
		this.shippingCountry = shippingCountry;
	}






	public String getShippingState() {
		return shippingState;
	}






	public void setShippingState(String shippingState) {
		this.shippingState = shippingState;
	}






	public BigDecimal getShippingAmountSingleProduct() {
		return shippingAmountSingleProduct;
	}






	public void setShippingAmountSingleProduct(
			BigDecimal shippingAmountSingleProduct) {
		this.shippingAmountSingleProduct = shippingAmountSingleProduct;
	}






	public BigDecimal getShippingAmountMultipleProduct() {
		return shippingAmountMultipleProduct;
	}






	public void setShippingAmountMultipleProduct(
			BigDecimal shippingAmountMultipleProduct) {
		this.shippingAmountMultipleProduct = shippingAmountMultipleProduct;
	}






	public BigDecimal getShippingTax() {
		return ShippingTax;
	}






	public void setShippingTax(BigDecimal shippingTax) {
		ShippingTax = shippingTax;
	}






	public ShippingApi() {
		super();

	}
	

 	
	
    
}