package com.mobicart.model;

import java.math.BigDecimal;

public class ProductOrderItemWebappMultiple {
	/**
	 * id genereted by database while insertion
	 */
	private Long id;
	/**
	 * {@link ProductOrder} id
	 */
	private Long orderId;
	/**
	 * {@link Product} id
	 */
	private Long productId;
	/**
	 *  {@link ProductOption} id
	 */
	private String productOptionId;
	
	/**
	 * Quantity of products bought
	 */
	private Integer iQuantity;
	
	/**
	 * price dedcuted for each product
	 */
	private BigDecimal fAmount;
	
	
	 /**
     * 
     * This field corresponds to the database column product_order_items_webapp.s_order_source
     *
     * 
     */
    private String sOrderSource;
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getOrderId() {
		return orderId;
	}

	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}

	public Long getProductId() {
		return productId;
	}

	
	public void setProductId(Long productId) {
		this.productId = productId;
	}

	
	

	public Integer getiQuantity() {
		return iQuantity;
	}

	
	public void setiQuantity(Integer iQuantity) {
		this.iQuantity = iQuantity;
	}

	
	public BigDecimal getfAmount() {
		return fAmount;
	}

	
	public void setfAmount(BigDecimal fAmount) {
		this.fAmount = fAmount;
	}

	
	
	
	
	public String getProductOptionId() {
		return productOptionId;
	}

	public void setProductOptionId(String productOptionId) {
		this.productOptionId = productOptionId;
	}

	
	
	public String getsOrderSource() {
		return sOrderSource;
	}

	public void setsOrderSource(String sOrderSource) {
		this.sOrderSource = sOrderSource;
	}

	@Override
	public String toString() {
		return "ProductOrderItemWebappMultiple [id=" + id + ", orderId=" + orderId
				+ ", productId=" + productId + ", productOptionId="
				+ productOptionId + ", iQuantity=" + iQuantity + ", fAmount="
				+ fAmount + "]";
	}

	
	
	
}