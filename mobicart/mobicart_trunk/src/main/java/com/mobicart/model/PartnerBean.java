package com.mobicart.model;

public class PartnerBean {
	
	String name;
	int freeUsers;
	int starterUsers;
	int proUsers;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getFreeUsers() {
		return freeUsers;
	}
	public void setFreeUsers(Integer freeUsers) {
		this.freeUsers = freeUsers;
	}
	public Integer getStarterUsers() {
		return starterUsers;
	}
	public void setStarterUsers(Integer starterUsers) {
		this.starterUsers = starterUsers;
	}
	public Integer getProUsers() {
		return proUsers;
	}
	public void setProUsers(Integer proUsers) {
		this.proUsers = proUsers;
	}
	

}
