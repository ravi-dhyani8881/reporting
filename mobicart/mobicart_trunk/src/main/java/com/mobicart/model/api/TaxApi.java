package com.mobicart.model.api;

import java.math.BigDecimal;

import org.codehaus.jackson.annotate.JsonWriteNullProperties;
@JsonWriteNullProperties(false)
public class TaxApi {
    
	private Long taxId;
	private String taxForState;
	private String taxForCountry;
	private String taxType;
	private BigDecimal taxRate;
	
	public Long getTaxId() {
		return taxId;
	}
	public void setTaxId(Long taxId) {
		this.taxId = taxId;
	}
	public String getTaxForState() {
		return taxForState;
	}
	public void setTaxForState(String taxForState) {
		this.taxForState = taxForState;
	}
	public String getTaxForCountry() {
		return taxForCountry;
	}
	public void setTaxForCountry(String taxForCountry) {
		this.taxForCountry = taxForCountry;
	}
	public String getTaxType() {
		return taxType;
	}
	public void setTaxType(String taxType) {
		this.taxType = taxType;
	}
	public BigDecimal getTaxRate() {
		return taxRate;
	}
	public void setTaxRate(BigDecimal taxRate) {
		this.taxRate = taxRate;
	}


 	
	
}