package com.mobicart.model.api;

import org.codehaus.jackson.annotate.JsonWriteNullProperties;

@JsonWriteNullProperties(false)
public class ProductImageApi {
	
	private Long imageId;
	private String title;
	private String location;
	private String locationMedium;
	private String locationSmall;
	
	public Long getImageId() {
		return imageId;
	}
	public void setImageId(Long imageId) {
		this.imageId = imageId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getLocationMedium() {
		return locationMedium;
	}
	public void setLocationMedium(String locationMedium) {
		this.locationMedium = locationMedium;
	}
	public String getLocationSmall() {
		return locationSmall;
	}
	public void setLocationSmall(String locationSmall) {
		this.locationSmall = locationSmall;
	}


}