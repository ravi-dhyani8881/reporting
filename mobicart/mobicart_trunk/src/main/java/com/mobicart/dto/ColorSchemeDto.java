package com.mobicart.dto;

import com.thoughtworks.xstream.annotations.XStreamAlias;

@XStreamAlias("colorscheme")
public class ColorSchemeDto {
    long id;
    private String title;
	private String type;
	private String theme_color;//background;
	private String header_color;//label; 
	private String sub_header_color;
	private String label_color;
	private String search_bar_bg_color;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTheme_color() {
		return theme_color;
	}
	public void setTheme_color(String theme_color) {
		this.theme_color = theme_color;
	}
	public String getHeader_color() {
		return header_color;
	}
	public void setHeader_color(String header_color) {
		this.header_color = header_color;
	}
	public String getSub_header_color() {
		return sub_header_color;
	}
	public void setSub_header_color(String sub_header_color) {
		this.sub_header_color = sub_header_color;
	}
	public String getLabel_color() {
		return label_color;
	}
	public void setLabel_color(String label_color) {
		this.label_color = label_color;
	}
	public String getSearch_bar_bg_color() {
		return search_bar_bg_color;
	}
	public void setSearch_bar_bg_color(String search_bar_bg_color) {
		this.search_bar_bg_color = search_bar_bg_color;
	}
 
	
	
}