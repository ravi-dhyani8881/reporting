package com.mobicart.util;


/**
 * Year helper class
 * @author jasdeep.singh
 *
 */

public class Year {

	private String value;

	public Year() {
		super();
	}

	public Year(String value) {
		super();
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	@Override
	public String toString() {
		return "Year [value=" + value + "]";
	}
	
	
	
}
